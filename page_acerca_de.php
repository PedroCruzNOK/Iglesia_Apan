La Iglesia Cristiana Pentecostal Ebenezer se estableció en la ciudad de Lowell en el año 1978 bajo la dirección del Reverendo Alfredo Maldonado. Para el año 1983 y tras el retiro del hermano Maldonado, el Movimiento Pentecostal Incorporado, al cual la iglesia se une, asigna a los hermanos José Uriel y Nélida Rivera como nuevos pastores de la iglesia y la iglesia obtienen el edificio que presentemente ocupa. Para el año 1990, la iglesia se separa del Movimiento Pentecostal Incorporado para establecerse como una iglesia independiente bajo el nombre de Centro Evangélico. Después de quince años de servicio a la comunidad y por razones de salud, el hermano Uriel decide retirarse del pastorado por tiempo indefinido. Tras la salida del hermano Uriel, el Centro Evangélico queda sin un pastor, siendo que esta es una iglesia sin afiliación conciliar. No obstante la congregación sigue trabajando, y orando por un nuevo líder, bajo la dirección del grupo de diáconos. En el 1998, la congregación recibe a los hermanos Israel y Julia Lozano como nuevos pastores de la misma.
<br>
<br>
Durante un año de arduo trabajo ministerial, reorganización del área administrativa de la iglesia, y antes de despedirse de la congregación, el Reverendo Lozano y la directiva deciden que la iglesia se una a un concilio. Después de considerar varias alternativas, la iglesia se une al Concilio General de las Asambleas de Dios a través del Distrito Hispano del Este de las Asambleas de Dios. En el mes de julio del 1999 la iglesia recibió a la familia Hernández como su nueva familia pastoral. De inmediato, la iglesia cambia su nombre a Iglesia Cristiana Ebenezer Asambleas de Dios.
